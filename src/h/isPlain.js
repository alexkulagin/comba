		const
			isObject = require('./isObject'),
			getPrototypeOf = require('./getPrototypeOf'),
			hasKey = require('./hasKey'),
			functionToString = require('./functionToString');



	//┐  IS PLAIN
	//╠──⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙⁘⁙
	//┘

		module.exports = (value) =>
		{
			if (!value || !isObject(value)) {
				return false;
			}

			const proto = getPrototypeOf(value);

			if (!proto) {
				return true;
			}

			const ƒ = hasKey(proto, 'constructor') && proto.constructor;

			return typeof ƒ === 'function' && ƒ instanceof ƒ && functionToString.call(ƒ) === functionToString.call(Object);
		};


